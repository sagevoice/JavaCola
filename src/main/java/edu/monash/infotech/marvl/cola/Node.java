package edu.monash.infotech.marvl.cola;

public class Node {
    /**
     * x and y will be computed by layout as the Node's centroid
     */
    public double x;
    /**
     * x and y will be computed by layout as the Node's centroid
     */
    public double y;
    /**
     * specify a width and height of the node's bounding box if you turn on avoidOverlaps
     */
    public double width;
    /**
     * specify a width and height of the node's bounding box if you turn on avoidOverlaps
     */
    public double height;

    public int index;
}
